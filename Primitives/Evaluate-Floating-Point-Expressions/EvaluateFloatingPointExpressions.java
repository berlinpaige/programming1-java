public class EvaluateFloatingPointExpressions {
   public static void main(String[] args) {
      System.out.println("5.0 + 3.5 = " + (5.0+3.5));   //prints out 5.0 + 3.5 = 8.5
      System.out.println("3.0 + 9.4 = " + (3.0+9.4)); //prints out 3.0 + 9.4 = 12.4
      System.out.println("16.4 - 5.2 = " + (16.4-5.2)); //prints out 16.4 - 5.2 = 11.2
      System.out.println("4.2 * 2.5 = " + (4.2*2.5)); //prints out 4.2 * 2.5 = 10.5
      System.out.println("5.0 / 2.0 = " + (5.0/2.0)); //prints out 5.0 / 2.0 = 2.5
      System.out.println("34.5 / 6.0 = " + (34.5/6.0)); //prints out 34.5 / 6.0 = 5.75
      System.out.println("34.5 % 6.0 = " + (34.5%6.0)); //prints out 34.5 % 6.0 = 4.5
      System.out.println("34.5 / 6.5 = " + (34.5/6.5)); //prints out 34.5 / 6.5 = 5.3076923076923075
      System.out.println("34.5 % 6.5 = " + (34.5%6.5)); //prints out 34.5 % 6.5 = 2.0
   }
}
